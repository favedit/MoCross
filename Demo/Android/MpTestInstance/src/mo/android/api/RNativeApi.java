package mo.android.api;

import android.content.res.AssetManager;

//============================================================
// <T>引擎库。</T>
//============================================================
public class RNativeApi
{
   //============================================================
   // <T>加载动态库。</T>
   //============================================================
   static{
      System.loadLibrary("MpCoolLight");
   }

   //============================================================
   // <T>设置环境。</T>
   //============================================================
   public static native void setEnvironment(String code,
                                            String value);

   //============================================================
   // <T>初始化处理。</T>
   //
   // @param assetManager 资源管理器
   //============================================================
   public static native void initialize(AssetManager assetManager);

   //============================================================
   // <T>配置处理。</T>
   //============================================================
   public static native void setup();

   //============================================================
   // <T>设置大小。</T>
   //============================================================
   public static native void resize(int width,
                                    int height);

   //============================================================
   // <T>循环处理。</T>
   //============================================================
   public static native void process();

   //============================================================
   // <T>释放处理。</T>
   //============================================================
   public static native void release();

   //============================================================
   // <T>鼠标落下处理。</T>
   //============================================================
   public static native void onMouseDown(int buttonCd,
                                         float x,
                                         float y);

   //============================================================
   // <T>鼠标移动处理。</T>
   //============================================================
   public static native void onMouseMove(int buttonCd,
                                         float x,
                                         float y);

   //============================================================
   // <T>鼠标抬起处理。</T>
   //============================================================
   public static native void onMouseUp(int buttonCd,
                                       float x,
                                       float y);
}
